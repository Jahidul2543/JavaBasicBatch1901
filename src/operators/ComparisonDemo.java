package operators;

public class ComparisonDemo {
    public static void main(String[] args){

        int value1 = 1;
        int value2 = 2;
        // check value1 is equal to value2

        /**
         * If I am feeling well I will play Cricket
         * if(yes/no, true/false)
         *
         * */


        if(value1 == value2)
            System.out.println("value1 == value2");


        if(value1 != value2)
            System.out.println("value1 != value2");
        if(value1 > value2)
            System.out.println("value1 > value2");
        if(value1 < value2)
            System.out.println("value1 < value2");
        // <=  less than or equal
        if(value1 <= value2)
            System.out.println("value1 <= value2");

    }

}
